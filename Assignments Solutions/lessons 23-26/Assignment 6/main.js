let flt = 10.4;

console.log(Number.parseInt(flt)); // 10
console.log(flt.toFixed(0)); // 10
console.log(Math.round(flt)); // 10
console.log(Math.floor(flt)); // 10
console.log(Math.trunc(flt)); // 10