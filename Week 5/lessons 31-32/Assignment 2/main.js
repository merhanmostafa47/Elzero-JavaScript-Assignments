 let num1 = 10;
let num2 = 20;

console.log(typeof num1 === typeof num2); // true
console.log(num2 > num1); // true
console.log(num1 < num2); // true
console.log(num1 != num2); // true
console.log(num2  !== num1); // true
console.log(`${num1}` <= `${num2}`); // true